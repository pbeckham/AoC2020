package main
import "fmt"
func main() {
	var piste = parseInput(input)

	var counts = []int{}
	var motions = []Point {
		Point { X: 1, Y: 1},
		Point { X: 1, Y: 3},
		Point { X: 1, Y: 5},
		Point { X: 1, Y: 7},
		Point { X: 2, Y: 1},
	}
	for _, motion := range motions {
		counts = append(counts, countTreesForMotion(piste, motion))
	}
	fmt.Println(counts)
	var multiplier = 1
	for _, count := range counts {
		multiplier = multiplier * count
	}
	fmt.Println(multiplier)
}

func countTreesForMotion(piste [][]bool, motion Point) int {
	var path = getPath(piste, Point { X: 0, Y: 0}, motion)
	var count = 0
	for _, isTree := range path {
		if isTree {
			count++
		}
	}
	return count
}


func parseInput(s string) [][]bool {
	var piste = [][]bool{}
	var currentRow = 0
	piste = append(piste, []bool{})
	for _, r := range s {
		if r == '\n' {
			currentRow++
			piste = append(piste, []bool{})
		} else {
			piste[currentRow] = append(piste[currentRow], r == '#')
		}
	}
	return piste
}

func getPath(piste [][]bool, position Point, motion Point) []bool {
	var newPosition = Point {
		X: position.X + motion.X,
		Y: (position.Y + motion.Y) % len(piste[0]),
	}
	if newPosition.X > len(piste) {
		return []bool{}
	}

	return append([]bool{piste[position.X][position.Y]}, getPath(piste, newPosition, motion)...)
}

type Point struct {
	X int
	Y int
}


